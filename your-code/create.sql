USE lab_mysql;

CREATE TABLE IF NOT EXISTS CARS(
ID INT PRIMARY KEY auto_increment,
VIN VARCHAR(100),
MANUFACTURER VARCHAR(50),
MODEL VARCHAR(50),
YEAR_ YEAR,
COLOR VARCHAR(50)
);


CREATE TABLE IF NOT EXISTS CUSTOMERS(
ID INT PRIMARY KEY auto_increment,
CUSTOMER_ID INT(100),
CUS_NAME VARCHAR(50),
PHONE VARCHAR(50),
EMAIL VARCHAR(50),
ADDRESS VARCHAR(50),
CITY VARCHAR(50),
State_Province VARCHAR(50),
Country VARCHAR(50),
Postal VARCHAR(50)
);

CREATE TABLE IF NOT EXISTS SALES_PERSON(
ID INT PRIMARY KEY auto_increment,
STAFF_ID INT,
NAME VARCHAR(50),
STORE VARCHAR(50)
);

CREATE TABLE IF NOT EXISTS INVOICES(
ID INT PRIMARY KEY auto_increment,
INVOICE_NUMBER INT,
DATE DATE,
MODEL VARCHAR(50),
CAR INT,
CUSTOMER INT,
SALES_PERSON INT
);
